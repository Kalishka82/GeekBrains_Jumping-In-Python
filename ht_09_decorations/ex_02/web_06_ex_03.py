    while amount > 0:
        num_user = int(input('–í–≤–µ–¥–∏—Ç–µ —á–∏—Å–ª–æ: '))
        if num_user == num:
            print('–ß–∏—Å–ª–æ —É–≥–∞–¥–∞–Ω–æ!')
            flag = True
            break
